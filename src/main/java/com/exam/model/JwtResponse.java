package com.exam.model;

//This will respond back the response after authenticating id and pass

public class JwtResponse {

    String token;

    public JwtResponse() {
    }

    public JwtResponse(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
